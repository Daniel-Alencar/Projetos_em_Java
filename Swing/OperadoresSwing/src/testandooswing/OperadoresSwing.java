        int n2 = Integer.parseInt(txtDen.getText());
        
        int divisao = n1/n2;
        double raiz = Math.sqrt((double)n1);
        
        lblDiv.setText(Integer.toString(divisao));
        lblRaiz.setText(Double.toString(raiz));
